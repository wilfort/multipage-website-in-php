« Les familles » en quelques mots…
Notre objectif est de vous offrir un lieu simple, un décor coloré qui ouvre les portes de l’imaginaire de vos enfants. Un lieu qui vous suggère de retrouver votre âme d’enfant, ou encore, d’échanger avec eux, pourquoi pas, votre expérience de vie.
Venir en famille au Tilt est une démarche multiple : un partage de temps, de jeux, de complicités et de connaissances.
Pour ce faire, notre espace explore les cinq continents et durant nos ouvertures aux familles, nous mettons plusieurs «explo-ateliers» en place. Ces explorations avec ateliers sont temporaires et mettent chaque fois un nouveau thème à l’honneur.
Il est donc possible de revenir plusieurs fois au Tilt en goûtant à de nouveaux ateliers, d’autres histoires, des aventures théâtrales différentes, de nouveaux jeux !
Notre décor général ne change pas mais il évolue. Nous avons voulu créer un «espace-cadre» où votre enfant sera content de revenir, et fier de ses repères. Tout en sachant qu’il se passe toujours quelque chose de neuf au Tilt.
Venez découvrir nos explo-ateliers, et partager avec vos petits loups des moments simples et nous l’espérons magiques.
Le Tilt c’est aussi : des questions, des bouquins, des moments câlins, des bricolages, un espace créatif et une scène de théâtre tout ça rien que pour vos petits, leurs amis et vous.</div>
